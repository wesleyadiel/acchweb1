	</span>
</p>
<p>
	<span class="p5">
		"Dallas também nos oferece a oportunidade de explorar um tipo diferente de rede de estradas para nossa tecnologia de direção autônoma", disse a empresa em seu post. "A infraestrutura moderna da cidade, padrões de tráfego exclusivos, características das estradas e clima oferecerão novas informações que podem melhorar nossos esforços contínuos de engenharia". 
